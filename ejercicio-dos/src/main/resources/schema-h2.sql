DROP TABLE IF EXISTS ITEM;
CREATE TABLE ITEM
(
	ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 10),
	NOMBRE VARCHAR(100) NOT NULL,
	TIPO_ITEM VARCHAR(10) NOT NULL,
	NEVERA BOOLEAN NOT NULL,
	STATUS VARCHAR(10) NOT NULL,
	USUARIO_CREADOR VARCHAR(50) NOT NULL,
	FECHA_REGISTRO TIMESTAMP NOT NULL,
	PRIMARY KEY (ID)
);


DROP TABLE IF EXISTS CAPACIDAD;
CREATE TABLE CAPACIDAD
(
	ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 10),
	ID_ITEM INT NOT NULL,
	CAPACIDAD_GR DOUBLE NOT NULL,	
	PRIMARY KEY (ID),
	FOREIGN KEY (ID_ITEM) REFERENCES ITEM (ID)
);


DROP TABLE IF EXISTS ENVASE;
CREATE TABLE ENVASE
(
	ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 10),
	ID_ITEM INT NOT NULL,
	TIPO_ENVASE VARCHAR(10) NOT NULL,
	PRIMARY KEY (ID),
	FOREIGN KEY (ID_ITEM) REFERENCES ITEM (ID)
);